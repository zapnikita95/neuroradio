const ru = fs.readFileSync(ruPath, 'utf8');
const re = /<string name="([^"]+)"([^>]*)>([\s\S]*?)<\/string>/g;
let m;
const lines = ['<?xml version="1.0" encoding="utf-8"?>', '<resources>'];
while ((m = re.exec(ru)) !== null) {
  const [, name, attrs, body] = m;
  const text = EN[name] ?? body;
  lines.push(`    <string name="${name}"${attrs}>${text}</string>`);
}
lines.push('</resources>', '');
fs.writeFileSync(enPath, lines.join('\n'), 'utf8');
console.log('wrote', enPath, 'keys', lines.length - 3);
